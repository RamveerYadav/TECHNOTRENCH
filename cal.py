# Calculator  Program

num1 = float(input("Enter the Number1:-"))
num2 = float(input("Enter the Number2:-"))
opr = input("Enter the Operator:-")
if opr == "+":
    print(num1+num2)
elif opr == "*":
    print(num1*num2)
elif opr == "-":
    print(num1-num2)  
elif opr == "/":
    print(num1/num2) 
else:
    print("Invalid")   
    


    